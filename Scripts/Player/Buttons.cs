/// <summary>
/// Represents the buttons that the player can press.
/// </summary>
public static class Buttons
{
    /// <summary>
    /// The button that makes the player crouch.
    /// </summary>
    public const string Crouch = nameof(Crouch);

    /// <summary>
    /// The button that makes the player jump.
    /// </summary>
    public const string Jump = nameof(Jump);

    /// <summary>
    /// The button that makes the player roll.
    /// </summary>
    public const string Roll = nameof(Roll);

    /// <summary>
    /// The button that makes the player perform a basic attack.
    /// </summary>
    public const string Fire1 = nameof(Fire1);

    /// <summary>
    /// The button that makes the player perform an elemental attack.
    /// </summary>
    public const string Fire2 = nameof(Fire2);

    /// <summary>
    /// The button that makes the player select the next element in the array.
    /// </summary>
    public const string SelectNextElem = nameof(SelectNextElem);

    /// <summary>
    /// The button that makes the the player select the previous element in the array.
    /// </summary>
    public const string SelectPrevElem = nameof(SelectPrevElem);

    /// <summary>
    /// The button that makes the the player interact with the environment.
    /// </summary>
    public const string Interact = nameof(Interact);

    public const string Inventory = nameof(Inventory);

    public const string Pause = nameof(Pause);
}
